char    *ft_strrev(char *str){
	char swap;
	int i = 0;
	int j = 0;

	while (str[i])
	{
		i++;
	}
	while (i > j)
	{
		swap = str[j];
		str[j] = str[i];
		str[i] = swap;
		j++;
		i--;
	}
	return (str);
}
