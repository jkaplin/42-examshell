#include <unistd.h>

void	print_bits(unsigned char octet)
{
	int		factor;

	factor = 256;
	while (factor >>= 1)
	{
		if (octet & factor)
			write(1, "1", 1);
		else
			write(1, "0", 1);
	}
}
